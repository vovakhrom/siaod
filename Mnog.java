package siaod1lab;

public class Mnog {
	int a;
	int n;
	public Mnog(int a, int n) {
		this.a = a;
		this.n = n;
	}
	public void setA(int a) {
		this.a = a;
	}

	public int getA() {
		return this.a;
	}

	public void setN(int n) {
		this.n = n;
	}

	public int getN() {
		return this.n;
	}
}
